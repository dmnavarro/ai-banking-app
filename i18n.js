function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) ?? I18N.en[key] ?? key;
}

function applyI18n(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  // Use data-i18n-html (not data-i18n) for strings that embed markup like <br> — textContent
  // would otherwise render the tags as literal text instead of formatting.
  scope.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = t(el.dataset.i18nHtml); });
  scope.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.dataset.i18nTitle); });
  scope.querySelectorAll('[data-i18n-aria-label]').forEach(el => { el.setAttribute('aria-label', t(el.dataset.i18nAriaLabel)); });
  scope.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.dataset.i18nPlaceholder); });
}
applyI18n();
document.getElementById('langSelect').value = currentLang;

// Re-renders every part of the UI that isn't purely data-i18n-driven: dashboard labels
// (without re-randomizing the underlying numbers), the AI Scanner objective/technique/
// modifier chips, and the guard pill text.
function setLanguage(lang) {
  if (!SUPPORTED_LANGS[lang] || lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
  applyI18n();
  renderDashboardTexts();
  renderObjectivesGrid();
  updateGuardPill();
}
